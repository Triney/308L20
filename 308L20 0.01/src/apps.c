void Main_Thread(void)
{
	uint8_t nop_i ;
	uint32_t aux_value;   

    LED_config();
	DisableSend485();
    
    Sft_Init_all_timers((void *) 0);
    while (1)
    {
        __WFI();
//		WDTFeed();

        sft_timer_thread();
        
        if(RcvNew)
        { 	
			dali_counter_1st=0;
			if(CheckSum(UARTBuf,TriggerNum-1)==UARTBuf[TriggerNum-1])
			{
				for(nop_i=0;nop_i<TriggerNum;nop_i++)
				{
					UARTBuffer[nop_i]=UARTBuf[nop_i]; 
				}
				if(BlockOption==0)
				{
					ProcessCMD();
				}
				else
				{
					EE_Block_Option();
					exit_block_option=0;
				}
			}
			RcvNew--;
			UARTCount=0;	
		}
    }
}

